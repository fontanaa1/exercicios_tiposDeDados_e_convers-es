// 1. Declare três variáveis: uma string, um número e um booleano. Exiba seus valores e tipos no console.
let nome = "João";         // String
let idade = 25;            // Número
let isAluno = true;        // Booleano

console.log(nome, typeof nome);    // "João" string
console.log(idade, typeof idade);  // 25 number
console.log(isAluno, typeof isAluno);  // true boolean

// 2. Converta um número em uma string e exiba o resultado e seu tipo.
let numero = 42;               // Número
let numeroString = String(numero);   // Conversão para string

console.log(numeroString, typeof numeroString);  // "42" string

// 3. Converta uma string numérica em um número e exiba o resultado e seu tipo.
let stringNumero = "123";       // String numérica
let numeroConvertido = Number(stringNumero);  // Conversão para número

console.log(numeroConvertido, typeof numeroConvertido);  // 123 number                                                                                                                                                                      